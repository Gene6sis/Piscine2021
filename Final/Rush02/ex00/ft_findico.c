int	ft_findico(char *str, char *nb)
{
	int	i;
	int	j;

	i = 0;
	while (str[i])
	{
		j = 0;
		while (nb[j] == str[i + j])
		{
			if (nb[j + 1] == '\0' && (str[i - 1] < '0' || str[i - 1] > '9') 
				&& (str[i + j + 1] < '0' || str[i + j + 1] > '9'))
				return (i);
			j++;
		}
		i++;
	}
	return (-1);
}

int main()
{
	printf("%d", ft_findico("86: quatre", "86"));
}
